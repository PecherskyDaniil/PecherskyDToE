
class response_formats:
    """
    Class for response formats
    """
    @staticmethod
    def csv() -> str:
        """
        csv format
        """
        return "csv"
    
    @staticmethod
    def xml() -> str:
        """
        xml format
        """
        return "xml"
    
    @staticmethod
    def json() -> str:
        """
        json format
        """
        return "json"

    @staticmethod
    def markdown() -> str:
        """
        markdown format
        """
        return "markdown"